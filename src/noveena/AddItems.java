    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        
        lblErr8.setText(null);
        String Search = txtSearch.getText();
        
        if (radioItemId.isSelected() || radioItemMake.isSelected() || radioItemName.isSelected()) {
            if (Validation.ValidTextAndDigits(Search)) {
                    if (radioItemId.isSelected()) {
                        tableLoad(Search,"ItemID");
                    }
                    else if(radioItemMake.isSelected()){
                        tableLoad(Search,"Make");
                    }
                    else if(radioItemName.isSelected()){
                        tableLoad(Search,"ItemName");
                    }
                    }
                    else
                        lblErr8.setText("*Invalid");  
        }
        else{
            JOptionPane.showMessageDialog(null,"No Option Selected","Error",JOptionPane.ERROR_MESSAGE);
                     
        }
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnSearchActionPerformed

