        public void tableLoad(String text,String selection){
            String sql="";
        try {
            if (selection=="Make") {
                sql = "SELECT ItemID,ItemName,Make,Model,Deposit,RentCharge,Availability From rentitem WHERE "+selection+" LIKE '%"+text+"%'"; 
            }
            else if (selection=="ItemName") {
                sql = "SELECT ItemID,ItemName,Make,Model,Deposit,RentCharge,Availability From rentitem WHERE "+selection+" LIKE '%"+text+"%'";  
            }
            else if (selection=="ItemID") {   
                sql = "SELECT ItemID,ItemName,Make,Model,Deposit,RentCharge,Availability From rentitem WHERE "+selection+" ='"+text+"'";
            }
            System.out.println(sql);
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (Exception e) {
        }
        
    }
        
