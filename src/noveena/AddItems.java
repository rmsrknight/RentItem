    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        lblErr7.setText(null);
        
        String ItemID = txtItemID.getText();
        String ItemName = txtItemName.getText();
        String Make = txtMake.getText();
        String Model = txtModel.getText();
        String Deposit = txtDeposit.getText();
        String RentCharge = txtRentCharge.getText();
        String Availability = comboAvailability.getSelectedItem().toString();
        
        if (ItemID.isEmpty() || ItemName.isEmpty() || Make.isEmpty() || Model.isEmpty() || Deposit.isEmpty() || RentCharge.isEmpty() || Availability.equals("Select")) {
                if(Availability.equals("Select")){
                lblErr7.setText("Availability Not Selected");
            }
            JOptionPane.showMessageDialog(null,"One or More Filelds Are Empty","Error",JOptionPane.ERROR_MESSAGE);
            
        }
        else if (ValidItemID(ItemID)) {
            JOptionPane.showMessageDialog(null,"Item ID Already Exist","Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(!ValidItemID(ItemID) && Validation.ValidTextOnly(Make) && Validation.ValidTextOnly(ItemName) && Validation.ValidTextAndDigits(Model) && Validation.ValidDigits(Deposit) && Validation.ValidDigits(RentCharge) && !Availability.equals("Select")){
                        try {
                            String query1 = "INSERT INTO rentitem (ItemID,ItemName,Make,Model,Deposit,RentCharge,Availability) VALUES ('"+ItemID+"', '"+ItemName+"', '"+Make+"', '"+Model+"', '"+Deposit+"', '"+RentCharge+"','"+Availability+"' )";
                            ps = con.prepareStatement(query1);
                            ps.execute();
                            JOptionPane.showMessageDialog(null,"Successfully Added","Success",JOptionPane.INFORMATION_MESSAGE);
                            btnClearActionPerformed(evt);
                                    
                        } catch (Exception e) {
                            System.out.println(e);
                        }  
        }
        else
            JOptionPane.showMessageDialog(null,"One Or More Fields Are Invalid","Error",JOptionPane.ERROR_MESSAGE);
        
        
        
        
    }//GEN-LAST:event_btnAddActionPerformed

